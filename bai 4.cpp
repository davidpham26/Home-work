#include <stdio.h>

int main() {
    // Khoi tao bien canh hinh vuông và gan gia tri 
    int canh = 5;
	 // Ðo dai canh hinh vuông va do dai 

    // Tính chu vi và dien tích hinh vuông
    int chuvi = 4 * canh;       
	// Chu vi = 4 * canh vi cong thuc chu vi la P=4 x canh 
    int dientich = canh * canh; 
	// Dien tích = canh * canh

    // Hien thi ket qua 
    printf("Canh hinh vuong: %d\n", canh);
    printf("Chu vi hinh vuong: %d\n", chuvi);
    printf("Dien tích hinh vuong: %d\n", dientich);
}

